function bin2image($binstring) { return str_replace("1", "#", str_replace("0", ".", $binstring)); }
function image2bin($binstring) { return str_replace("#", "1", str_replace(".", "0", $binstring)); }
function printimg($img) {
  foreach ($img as $line) {
    foreach ($line as $char) {
      echo $char === "1" ? "#" : ".";
    }
    echo "\n";
  }
}
function lit($img) {
  return collect($img)->map(fn($line) => collect($line)->map(fn($x) => intval($x))->sum())->sum();
}

$data = preg_split("/\r?\n/", trim(image2bin($input)));

$algo = "";
$rest = false;
$img = [];
foreach ($data as $line) {
  if (empty($line)) $rest = true;
  else if (!$rest) {
    $algo .= $line;
  }
  else {
    $row = [];
    foreach (str_split($line) as $char) {
      array_push($row, $char);
    }
    array_push($img, $row);
  }
}

function solvePart1($algo, $img) {

  $minx = min(array_keys($img)) - 4;
  $maxx = max(array_keys($img)) + 5;
  $miny = min(array_keys($img[0])) - 4;
  $maxy = max(array_keys($img[0])) + 5;

  for ($loop = 0; $loop < 2; $loop++) {
    echo "Loop nr $loop\n-------------\n";

    $newimg = [];
    for ($y = $miny; $y < $maxy; $y++) {
      for ($x = $minx; $x < $maxx; $x++) {

        $pattern = "";

        if (array_key_exists($y-1, $img) && array_key_exists($x-1, $img[$y-1])) $pattern .= $img[$y-1][$x-1]; else $pattern .= "0";
        if (array_key_exists($y-1, $img) && array_key_exists($x  , $img[$y-1])) $pattern .= $img[$y-1][$x  ]; else $pattern .= "0";
        if (array_key_exists($y-1, $img) && array_key_exists($x+1, $img[$y-1])) $pattern .= $img[$y-1][$x+1]; else $pattern .= "0";
        
        if (array_key_exists($y  , $img) && array_key_exists($x-1, $img[$y  ])) $pattern .= $img[$y  ][$x-1]; else $pattern .= "0";
        if (array_key_exists($y  , $img) && array_key_exists($x  , $img[$y  ])) $pattern .= $img[$y  ][$x  ]; else $pattern .= "0";
        if (array_key_exists($y  , $img) && array_key_exists($x+1, $img[$y  ])) $pattern .= $img[$y  ][$x+1]; else $pattern .= "0";
        
        if (array_key_exists($y+1, $img) && array_key_exists($x-1, $img[$y+1])) $pattern .= $img[$y+1][$x-1]; else $pattern .= "0";
        if (array_key_exists($y+1, $img) && array_key_exists($x  , $img[$y+1])) $pattern .= $img[$y+1][$x  ]; else $pattern .= "0";
        if (array_key_exists($y+1, $img) && array_key_exists($x+1, $img[$y+1])) $pattern .= $img[$y+1][$x+1]; else $pattern .= "0";

        $number = bindec($pattern);
        $newchar = substr($algo, $number, 1);

        if (!array_key_exists($y, $newimg)) $newimg[$y] = [];
        $newimg[$y][$x] = $newchar;
        
      }
    }
    echo "\n" . printimg($newimg) . "\n";
    $img = $newimg;
  }

  return lit($img);
}

